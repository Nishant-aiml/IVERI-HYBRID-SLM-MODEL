"""Response quality inspector for IVERI CORE SFT evaluation.

Automatically detects quality problems in generated byte-level model responses:

- **Repetition** — repeated n-gram loops
- **Collapse** — all bytes are the same or very low entropy
- **Empty output** — no non-padding bytes generated
- **UTF-8 corruption** — invalid byte sequences
- **Excessive loops** — repeating identical substrings
- **Invalid bytes** — unexpected padding or null bytes
- **Entropy** — byte distribution entropy (higher = more diverse)
- **Average response length** — mean number of non-padding bytes

Examples
--------
>>> from evaluation.response_inspector import ResponseInspector
>>> insp = ResponseInspector()
>>> report = insp.inspect_bytes(b"Hello, world!")
>>> report["is_valid"]
True
>>> report["entropy"] > 0
True
"""

from __future__ import annotations

import logging
import math
from collections import Counter
from dataclasses import dataclass, field
from typing import Any

from core.byte_vocab import strip_special_bytes
from core.constants import LEGACY_PAD_BYTE

# ── Constants ──────────────────────────────────────────────────────────────

_MIN_ENTROPY_THRESHOLD: float = 0.5
"""Min Shannon entropy (bits/byte) below which output is considered collapsed."""

_MAX_REPETITION_RATIO: float = 0.7
"""If top n-gram coverage > this, output is considered repetitive."""

_MIN_RESPONSE_BYTES: int = 4
"""Minimum non-padding bytes to consider a response non-empty."""


# ── Inspection result ──────────────────────────────────────────────────────


@dataclass
class InspectionResult:
    """Quality inspection result for a single response.

    Attributes
    ----------
    is_valid:
        ``True`` if no quality problems detected.
    issues:
        List of detected issue names (e.g. ``"repetition"``, ``"collapse"``).
    entropy:
        Shannon entropy (bits/byte) of the response.
    length:
        Non-padding byte count.
    utf8_valid:
        Whether the response decodes cleanly as UTF-8.
    repetition_ratio:
        Fraction of bytes covered by the most common bigram.
    has_excessive_loops:
        ``True`` if any substring ≥ 8 bytes repeats ≥ 5 times.
    is_empty:
        ``True`` if response has fewer than ``_MIN_RESPONSE_BYTES`` non-pad bytes.
    is_collapsed:
        ``True`` if entropy < ``_MIN_ENTROPY_THRESHOLD``.
    decoded_text:
        UTF-8-decoded text (with replacement chars for invalid bytes).
    """

    is_valid: bool = True
    issues: list[str] = field(default_factory=list)
    entropy: float = 0.0
    length: int = 0
    utf8_valid: bool = True
    repetition_ratio: float = 0.0
    has_excessive_loops: bool = False
    is_empty: bool = False
    is_collapsed: bool = False
    decoded_text: str = ""


# ── Inspector class ────────────────────────────────────────────────────────


class ResponseInspector:
    """Inspect model responses for quality issues.

    Parameters
    ----------
    min_entropy:
        Entropy threshold below which responses are flagged as collapsed.
    max_repetition_ratio:
        Bigram coverage ratio above which responses are flagged as repetitive.
    min_response_bytes:
        Minimum non-padding bytes for a non-empty response.
    loop_min_len:
        Minimum substring length to check for loops.
    loop_min_count:
        Minimum repeats to flag as excessive loop.
    """

    def __init__(
        self,
        min_entropy: float = _MIN_ENTROPY_THRESHOLD,
        max_repetition_ratio: float = _MAX_REPETITION_RATIO,
        min_response_bytes: int = _MIN_RESPONSE_BYTES,
        loop_min_len: int = 8,
        loop_min_count: int = 5,
    ) -> None:
        self.min_entropy = min_entropy
        self.max_repetition_ratio = max_repetition_ratio
        self.min_response_bytes = min_response_bytes
        self.loop_min_len = loop_min_len
        self.loop_min_count = loop_min_count

    # ── Public API ─────────────────────────────────────────────────────

    def inspect_bytes(self, response: bytes) -> InspectionResult:
        """Inspect a raw byte response.

        Parameters
        ----------
        response:
            Raw generated bytes.

        Returns
        -------
        InspectionResult
        """
        # Strip padding
        stripped = bytes(strip_special_bytes(list(response)))
        if not stripped and response:
            stripped = response.rstrip(bytes([LEGACY_PAD_BYTE]))
        n = len(stripped)

        result = InspectionResult()
        issues: list[str] = []

        # 1. Empty check
        result.is_empty = n < self.min_response_bytes
        if result.is_empty:
            issues.append("empty")

        # 2. UTF-8 validity
        try:
            decoded = stripped.decode("utf-8")
            result.utf8_valid = True
        except UnicodeDecodeError:
            decoded = stripped.decode("utf-8", errors="replace")
            result.utf8_valid = False
            issues.append("utf8_corruption")
        result.decoded_text = decoded

        if n == 0:
            result.is_valid = len(issues) == 0
            result.issues = issues
            result.length = 0
            return result

        # 3. Entropy
        entropy = _byte_entropy(stripped)
        result.entropy = entropy
        result.is_collapsed = entropy < self.min_entropy
        if result.is_collapsed:
            issues.append("collapse")

        # 4. Repetition (bigram coverage)
        rep_ratio = _bigram_repetition_ratio(stripped)
        result.repetition_ratio = rep_ratio
        if rep_ratio > self.max_repetition_ratio:
            issues.append("repetition")

        # 5. Excessive loops (repeated substrings)
        has_loops = _has_excessive_loops(
            stripped, self.loop_min_len, self.loop_min_count
        )
        result.has_excessive_loops = has_loops
        if has_loops:
            issues.append("excessive_loops")

        # 6. Invalid bytes (only null bytes in stripped content)
        if LEGACY_PAD_BYTE.to_bytes(1, "big") in stripped:
            issues.append("invalid_bytes")

        result.length = n
        result.issues = issues
        result.is_valid = len(issues) == 0
        return result

    def inspect_text(self, text: str) -> InspectionResult:
        """Inspect a decoded text response.

        Parameters
        ----------
        text:
            Decoded text string.

        Returns
        -------
        InspectionResult
        """
        return self.inspect_bytes(text.encode("utf-8", errors="replace"))

    def inspect_batch(self, responses: list[bytes]) -> dict[str, Any]:
        """Inspect a batch of responses and compute aggregate statistics.

        Parameters
        ----------
        responses:
            List of raw byte responses.

        Returns
        -------
        dict[str, Any]
            Aggregate statistics including per-issue counts and averages.
        """
        results = [self.inspect_bytes(r) for r in responses]

        total = len(results)
        if total == 0:
            return {
                "total": 0,
                "valid_count": 0,
                "valid_ratio": 0.0,
                "issue_counts": {},
                "avg_entropy": 0.0,
                "avg_length": 0.0,
                "avg_repetition_ratio": 0.0,
            }

        valid_count = sum(1 for r in results if r.is_valid)
        issue_counter: Counter[str] = Counter()
        for r in results:
            for issue in r.issues:
                issue_counter[issue] += 1

        return {
            "total": total,
            "valid_count": valid_count,
            "valid_ratio": valid_count / total,
            "issue_counts": dict(issue_counter),
            "avg_entropy": sum(r.entropy for r in results) / total,
            "avg_length": sum(r.length for r in results) / total,
            "avg_repetition_ratio": sum(r.repetition_ratio for r in results) / total,
            "empty_count": sum(1 for r in results if r.is_empty),
            "collapse_count": sum(1 for r in results if r.is_collapsed),
            "utf8_error_count": sum(1 for r in results if not r.utf8_valid),
            "loop_count": sum(1 for r in results if r.has_excessive_loops),
            "individual": [
                {
                    "is_valid": r.is_valid,
                    "issues": r.issues,
                    "entropy": r.entropy,
                    "length": r.length,
                    "repetition_ratio": r.repetition_ratio,
                }
                for r in results
            ],
        }

    def score_response(self, response: bytes) -> float:
        """Compute a scalar quality score in [0, 1].

        Higher = better quality.

        Parameters
        ----------
        response:
            Raw generated bytes.

        Returns
        -------
        float
            Quality score between 0.0 (worst) and 1.0 (best).
        """
        result = self.inspect_bytes(response)
        if result.is_empty:
            return 0.0
        if result.is_collapsed:
            return 0.1
        # Penalize per issue
        penalty = len(result.issues) * 0.15
        # Reward entropy (normalize to [0, 1]; max entropy for bytes = log2(256) = 8)
        entropy_score = min(result.entropy / 8.0, 1.0)
        base_score = max(0.0, 1.0 - penalty)
        return (base_score + entropy_score) / 2.0


# ── Private helpers ────────────────────────────────────────────────────────


def _byte_entropy(data: bytes) -> float:
    """Compute Shannon entropy of a byte sequence (bits per byte)."""
    if not data:
        return 0.0
    counts = Counter(data)
    n = len(data)
    entropy = 0.0
    for cnt in counts.values():
        p = cnt / n
        entropy -= p * math.log2(p)
    return entropy


def _bigram_repetition_ratio(data: bytes) -> float:
    """Compute the fraction of bytes covered by the most common bigram."""
    if len(data) < 2:
        return 0.0
    bigrams = [data[i : i + 2] for i in range(len(data) - 1)]
    if not bigrams:
        return 0.0
    counts = Counter(bigrams)
    top_count = counts.most_common(1)[0][1]
    # Each bigram covers 2 bytes; top_count occurrences cover top_count * 2 positions
    return min(top_count * 2 / len(data), 1.0)


def _has_excessive_loops(data: bytes, min_len: int = 8, min_count: int = 5) -> bool:
    """Detect if any substring of length >= min_len repeats >= min_count times."""
    n = len(data)
    for substr_len in range(min_len, min(64, n // min_count + 1)):
        seen: Counter[bytes] = Counter()
        for i in range(n - substr_len + 1):
            seen[data[i : i + substr_len]] += 1
        if seen and seen.most_common(1)[0][1] >= min_count:
            return True
    return False
